let count = 0;
let index = 0;
let currentText = "";
let letter = "";

(function type() {
  if (count === texts.length) {
    count = 0;
  }

  currentText = texts[count];

  letter = currentText.slice(0, ++index);

  document.querySelector(".typing").textContent = letter;
  if (letter.length == currentText.length) {
    count++;
    index = 0;
  }
  setTimeout(type, 300);
})();

const navSlide = () => {
  const burger = document.querySelector(".burger");
  const nav = document.querySelector(".nav-links");
  const navLinks = document.querySelectorAll(".nav-links li");

  burger.addEventListener("click", () => {
    //Toggle Nav
    nav.classList.toggle("nav-active");

    //animate links
    navLinks.forEach((link, index) => {
      if (link.style.animation) {
        link.style.animation = "";
      } else {
        link.style.animation = `navLinkFade 0.5s ease forwards ${
          index / 5 + 0.5
        }s`;
      }
    });
    burger.classList.toggle("toggle");
  });
};

navSlide();

function scrollAppear() {
  var introText = document.querySelector(".intro-text-animation");
  var introImg = document.querySelector(".img");
  var introPosition = introText.getBoundingClientRect().top;
  // getBoundingClientRect() A DOMRect object with eight properties: left, top, right, bottom, x, y, width, height.
  var screenPosition = window.innerHeight / 1.5;
  //The innerHeight property returns the height of a window's content area.

  if (introPosition < screenPosition) {
    introText.classList.add("intro-appear-animation");
    introImg.classList.add("intro-appear-animation");
  }
}

window.addEventListener("scroll", scrollAppear);

